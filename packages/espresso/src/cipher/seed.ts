function gFunction(x: number, ss0: Uint32Array, ss1: Uint32Array, ss2: Uint32Array, ss3: Uint32Array): number {
  return (
    ss0[(x >>> 24) & 0xff] ^
    ss1[((x >>> 16) & 0xff) + 256] ^
    ss2[(x >>> 8) & 0xff] ^
    ss3[(x & 0xff) + 256]
  );
}

function mix(a: number, b: number, ss0: Uint32Array, ss1: Uint32Array, ss2: Uint32Array, ss3: Uint32Array): number {
  const t = gFunction(a, ss0, ss1, ss2, ss3);
  const t2 = t + gFunction(b, ss0, ss1, ss2, ss3);
  return (((t2 << 8) | (t2 >>> 24)) + t) >>> 0;
}

function unmix(a: number, b: number, ss0: Uint32Array, ss1: Uint32Array, ss2: Uint32Array, ss3: Uint32Array): number {
  const t = gFunction(a, ss0, ss1, ss2, ss3);
  const t2 = t + gFunction(b, ss0, ss1, ss2, ss3);
  const t3 = t2 - t;
  return (((t2 << 24) | (t2 >>> 8)) ^ t3) >>> 0;
}

function seedBlock(data: Uint8Array, key: Uint8Array, doEncrypt: boolean): Uint8Array {
  const k = new Uint32Array(4);
  for (let i = 0; i < 4; i++) {
    k[i] = ((key[i * 4] << 24) | (key[i * 4 + 1] << 16) | (key[i * 4 + 2] << 8) | key[i * 4 + 3]) >>> 0;
  }

  const keys = new Uint32Array(32);
  for (let i = 0; i < 32; i++) {
    keys[i] = KEYSCHEDULE[i & 0xf] + k[i & 3];
    keys[i] = keys[i] >>> 0;
  }

  const result = new Uint8Array(data.length);
  for (let offset = 0; offset < data.length; offset += 16) {
    const left = new Uint32Array(2);
    const right = new Uint32Array(2);

    left[0] = ((data[offset] << 24) | (data[offset + 1] << 16) | (data[offset + 2] << 8) | data[offset + 3]) >>> 0;
    left[1] = ((data[offset + 4] << 24) | (data[offset + 5] << 16) | (data[offset + 6] << 8) | data[offset + 7]) >>> 0;
    right[0] = ((data[offset + 8] << 24) | (data[offset + 9] << 16) | (data[offset + 10] << 8) | data[offset + 11]) >>> 0;
    right[1] = ((data[offset + 12] << 24) | (data[offset + 13] << 16) | (data[offset + 14] << 8) | data[offset + 15]) >>> 0;

    if (doEncrypt) {
      for (let round = 0; round < 16; round++) {
        const r = round * 2;
        right[0] ^= mix(left[0] + keys[r], left[1] + keys[r + 1], SS0, SS1, SS2, SS3);
        right[0] = right[0] >>> 0;
        right[1] ^= mix(left[0] + keys[r], left[1] + keys[r + 1], SS0, SS1, SS2, SS3);
        right[1] = right[1] >>> 0;
        left[0] ^= mix(right[0] + keys[r + 2], right[1] + keys[r + 3], SS0, SS1, SS2, SS3);
        left[0] = left[0] >>> 0;
        left[1] ^= mix(right[0] + keys[r + 2], right[1] + keys[r + 3], SS0, SS1, SS2, SS3);
        left[1] = left[1] >>> 0;
      }
    } else {
      for (let round = 15; round >= 0; round--) {
        const r = round * 2;
        left[0] ^= unmix(right[0] + keys[r + 2], right[1] + keys[r + 3], SS0, SS1, SS2, SS3);
        left[0] = left[0] >>> 0;
        left[1] ^= unmix(right[0] + keys[r + 2], right[1] + keys[r + 3], SS0, SS1, SS2, SS3);
        left[1] = left[1] >>> 0;
        right[0] ^= unmix(left[0] + keys[r], left[1] + keys[r + 1], SS0, SS1, SS2, SS3);
        right[0] = right[0] >>> 0;
        right[1] ^= unmix(left[0] + keys[r], left[1] + keys[r + 1], SS0, SS1, SS2, SS3);
        right[1] = right[1] >>> 0;
      }
    }

    result[offset] = (left[0] >>> 24) & 0xff;
    result[offset + 1] = (left[0] >>> 16) & 0xff;
    result[offset + 2] = (left[0] >>> 8) & 0xff;
    result[offset + 3] = left[0] & 0xff;
    result[offset + 4] = (left[1] >>> 24) & 0xff;
    result[offset + 5] = (left[1] >>> 16) & 0xff;
    result[offset + 6] = (left[1] >>> 8) & 0xff;
    result[offset + 7] = left[1] & 0xff;
    result[offset + 8] = (right[0] >>> 24) & 0xff;
    result[offset + 9] = (right[0] >>> 16) & 0xff;
    result[offset + 10] = (right[0] >>> 8) & 0xff;
    result[offset + 11] = right[0] & 0xff;
    result[offset + 12] = (right[1] >>> 24) & 0xff;
    result[offset + 13] = (right[1] >>> 16) & 0xff;
    result[offset + 14] = (right[1] >>> 8) & 0xff;
    result[offset + 15] = right[1] & 0xff;
  }
  return result;
}

export function seedEncrypt(data: Uint8Array, key: Uint8Array, opts: CipherOptions): Uint8Array {
  const padding = opts.padding ?? 'pkcs7';
  const padded = padding === 'pkcs7' ? pkcs7Pad(data, 16) : data;
  return seedBlock(padded, key, true);
}

export function seedDecrypt(data: Uint8Array, key: Uint8Array, opts: CipherOptions): Uint8Array {
  const result = seedBlock(data, key, false);
  const padding = opts.padding ?? 'pkcs7';
  if (padding === 'pkcs7') return pkcs7Unpad(result);
  return result;
}
